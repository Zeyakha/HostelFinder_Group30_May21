package hostelfinder.entites;

public class HostelInfo {
	private String hostelname;
	private String nearcollege;
	private String hosteltype;
	private String hosteldes;
	private String city;
	private String area;

	public String getHostelname() {
		return hostelname;
	}

	public void setHostelname(String hostelname) {
		this.hostelname = hostelname;
	}

	public String getNearcollege() {
		return nearcollege;
	}

	public void setNearcollege(String nearcollege) {
		this.nearcollege = nearcollege;
	}

	public String getHosteltype() {
		return hosteltype;
	}

	public void setHosteltype(String hosteltype) {
		this.hosteltype = hosteltype;
	}

	public String getHosteldes() {
		return hosteldes;
	}

	public void setHosteldes(String hosteldes) {
		this.hosteldes = hosteldes;
	}

	public String getCity() {
		return city;
	}

	public void setCity(String city) {
		this.city = city;
	}

	public String getArea() {
		return area;
	}

	public void setArea(String area) {
		this.area = area;
	}

}
